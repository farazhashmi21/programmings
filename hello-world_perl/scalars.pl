#!usr/bin/perl
# Scalar Variables
# Variables with no indexes or only have zero index is called scalar variable
$num = 46;
print("\n\$num is: $num");
$string = "Text Literal";
print("\n\$string is: $string");
$var = 2.86;
print("\n\$var is: $var");
$sum = ($num + $var);
print("\n\$sum is: $sum");
$L = "I";
$U = "LOVE";
$V = "YOU";
$LOVE = $L." ".$U." ".$V;
print("\n$LOVE");
$ConcaT = $string." ".$num;
print("\n$ConcaT");
